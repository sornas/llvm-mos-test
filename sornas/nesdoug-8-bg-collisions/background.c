const uint8_t *const bg_data[] = {c1, c2, c3, c4};
uint8_t sel = 0;

void bg_next() { sel = (sel + 1) % 4; }

struct bg_collision bg_check_collision(struct box *box) {
  const uint8_t x1 = box->x, x2 = box->x + box->w;
  const uint8_t y1 = box->y, y2 = box->y + box->h;
  const uint8_t *const bg = bg_data[sel];

  struct bg_collision res = {0, 0, 0, 0};
  if (y1 >= 0xf0)
    return res;

  if (bg[IDX(x1, y1)]) {
    res.left++;
    res.up++;
  }

  if (bg[IDX(x2, y1)]) {
    res.right++;
    res.up++;
  }

  if (y2 >= 0xf0)
    return res;

  if (bg[IDX(x1, y2)]) {
    res.left++;
    res.down++;
  }

  if (bg[IDX(x2, y2)]) {
    res.right++;
    res.down++;
  }

  return res;
}

void bg_draw() {
  const uint8_t *bg = bg_data[sel];

  ppu_off();

  vram_adr(NAMETABLE_A);

#define CHECK_ROW(X, Y)                                                        \
  do {                                                                         \
    uint8_t idx = (Y << 4) + X;                                                \
    if (bg[idx]) {                                                             \
      vram_put(0x10);                                                          \
      vram_put(0x10);                                                          \
    } else {                                                                   \
      vram_put(0x00);                                                          \
      vram_put(0x00);                                                          \
    }                                                                          \
  } while (0)

  for (uint8_t y = 0; y < 15; y++) {
    for (uint8_t x = 0; x < 16; x++)
      CHECK_ROW(x, y);
    for (uint8_t x = 0; x < 16; x++)
      CHECK_ROW(x, y);
  }
#undef CHECK_ROW

  ppu_on_all();
}
